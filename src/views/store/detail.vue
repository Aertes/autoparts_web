<script>
import { storeDetail } from '@/api/Api'
export default {
  name: 'detail',
  props: {
    dialogDetailShow: {
      type: Boolean,
      default: false
    },
    storeId: {
      type: Number,
      default: 0
    }
  },
  data() {
    return {
      storeData: ''
    }
  },
  computed: {
    dialogDetailStoreVisible: {
      get() {
        if (this.dialogDetailShow) {
          this.detail()
        }
        return this.dialogDetailShow
      },
      set(value) {
        if (!value) {
          this.$emit('close')
        }
      }
    }
  },
  methods: {
    detail() {
      const data = { apcid: this.storeId }
      return new Promise((resolve, reject) => {
        storeDetail(data).then(res => {
          if (res.status === 0) {
            console.log(res.data)
            this.storeData = res.data
          } else {
            this.$notify({
              showClose: true,
              message: res.msg,
              type: 'warning',
              offset: 100,
              duration: 2000
            })
          }
        }).catch(error => {
          reject(error)
        })
      })
    },
    clearData() {
